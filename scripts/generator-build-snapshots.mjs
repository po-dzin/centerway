function toSnapshotEntry(scenario, resolved) {
  return {
    id: scenario.id,
    route_key: scenario.routeKey,
    screen_id: resolved.screen.id,
    screen_version: resolved.screen.version,
    mode: resolved.screen.mode,
    branch: resolved.screen.branch,
    experiment: resolved.experimentResolution,
    blocks: resolved.blocks.map((block) => ({
      id: block.id,
      recipe_id: block.recipe.id,
      recipe_version: block.recipe.version,
      component_key: block.recipe.component_key,
      props: block.props,
    })),
    tokens: resolved.tokens,
  };
}

function buildTokensCss(entries) {
  const lines = [
    "/* generated by scripts/generator-build-snapshots.mjs */",
    "",
  ];

  for (const entry of entries) {
    lines.push(`/* ${entry.id} */`);
    lines.push(`[data-cw-screen-id=\"${entry.screen_id}\"] {`);
    for (const tokenName of Object.keys(entry.tokens).sort()) {
      lines.push(`  ${tokenName}: ${entry.tokens[tokenName]};`);
    }
    lines.push("}");
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

function buildMetadataTs(entries) {
  const meta = entries.map((entry) => ({
    id: entry.id,
    route_key: entry.route_key,
    screen_id: entry.screen_id,
    screen_version: entry.screen_version,
    mode: entry.mode,
    branch: entry.branch,
    experiment_key: entry.experiment?.experiment_key ?? null,
    variant_key: entry.experiment?.variant_key ?? null,
  }));

  return [
    "// generated by scripts/generator-build-snapshots.mjs",
    `export const cwGeneratorSnapshots = ${stableStringify(meta)} as const;`,
    "",
  ].join("\n");
}

async function main() {
  const { generatorDir, manifests } = await loadGeneratorManifests();
  const validation = validateGeneratorManifests(manifests);

  const entries = SCENARIOS.map((scenario) => {
    const resolved = resolveScreenForRoute(validation, scenario.routeKey, scenario.assignments);
    return toSnapshotEntry(scenario, resolved);
  });

  const snapshotsDir = path.join(generatorDir, "snapshots");
  await fs.mkdir(snapshotsDir, { recursive: true });

  const screensJsonPath = path.join(snapshotsDir, "screens.generated.json");
  const tokensCssPath = path.join(snapshotsDir, "tokens.generated.css");
  const metadataTsPath = path.join(snapshotsDir, "metadata.generated.ts");

  await fs.writeFile(screensJsonPath, `${stableStringify(entries)}\n`, "utf8");
  await fs.writeFile(tokensCssPath, buildTokensCss(entries), "utf8");
  await fs.writeFile(metadataTsPath, buildMetadataTs(entries), "utf8");

  console.log("Generator snapshots generated");
  console.log(screensJsonPath);
  console.log(tokensCssPath);
  console.log(metadataTsPath);
}

main().catch((error) => {
  console.error("Generator snapshot build failed");
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
